function calculateStarIndex(stars) {
  const sorted = [...stars].sort((a, b) => b.impactScore - a.impactScore);
  const weighted = sorted.reduce((sum, star, index) => sum + star.impactScore * [0.36, 0.28, 0.21, 0.15][index], 0);
  return Math.round(weighted * 10) / 10;
}
