export function summarizeNationalStars(profiles = NATIONAL_STAR_PROFILES) {
  const enriched = profiles.map((profile) => ({
    ...profile,
    starIndex: calculateStarIndex(profile.stars),
    topStar: [...profile.stars].sort((a, b) => b.impactScore - a.impactScore)[0]
  }));

  return {
    profiles: enriched.sort((a, b) => b.starIndex - a.starIndex),
    topProfile: enriched.reduce((best, profile) => (profile.starIndex > best.starIndex ? profile : best), enriched[0]),
    totalCountries: enriched.length,
    totalStars: enriched.reduce((sum, profile) => sum + profile.stars.length, 0)
  };
}

function star(name, position, club, form, influence, availability, trend) {
  const metadata = starMetadata(name);
  return {
    name,
    shortName: metadata.shortName ?? name.split(" ").at(-1),
    chineseName: metadata.chineseName ?? "中文待核",
    marketValueEurM: metadata.marketValueEurM ?? null,
    position,
    club,
    form,
    influence,
    availability,
    trend,
    impactScore: Math.round(((form * 0.42 + influence * 0.38 + availability * 0.2) / 100) * 1000) / 10
  };
}

